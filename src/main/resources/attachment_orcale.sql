create table ATTACHMENT 
(
  "ID" INTEGER primary key,
  "CONTENT_TYPE" VARCHAR2(10) not null,
  "CONTENT_NAME" VARCHAR2(20) not null,
  "RESOURCE_ID" VARCHAR2(10) not null,
  "RESOURCE_NAME" VARCHAR2(20) not null,
  "TARGET_ID" VARCHAR2(20) not null,
  "TARGET_NAME" VARCHAR2(20) not null,
  "TEMP_ID" INTEGER
);

CREATE SEQUENCE  "ATTACHMENT_ID_SEQ"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1;

create table ATTACHMENT_TEMP
(
  "ID" INTEGER primary key,
  "DATA_FILE" blob
);

CREATE SEQUENCE  "ATTACHMENT_TEMP_ID_SEQ"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1;